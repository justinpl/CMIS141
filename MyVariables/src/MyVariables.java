public class MyVariables 
{
	public static void main(String[] args) 
	{
		// Declare and initialize primitive variables
		boolean myBoolt = true;
		boolean myBoolf = false;
		double myDouble1 = 42.945;
		double myDouble2 = 62.687;
		int myInt1 = 42;
		int myInt2 = 100;
		int myInt3 = 8_000_000;
		char myChar1 = 'A';
		char myChar2 = 'Z';
		int myHex = 0x002F;
		int myBin = 0b1101;		
		
		// Print each variable along with the value
		System.out.println("myBoolt: " + myBoolt);
		System.out.println("myBoolf: " + myBoolf);
		System.out.println("myDouble1: " + myDouble1);
		System.out.println("myDouble2: " + myDouble2);
		System.out.println("myInt1: " + myInt1);
		System.out.println("myInt2: " + myInt2);
		System.out.println("myInt3: " + myInt3);
		System.out.println("myChar1: " + myChar1);
		System.out.println("myChar2: " + myChar2);
		System.out.println("myHex: " + myHex);
		System.out.println("myBin: " + myBin);
	}
}